									période “T”. Et la formule magique est … M * V = P * T. Un des grands débats entre économistes de différents courants de
									pensée depuis des dizaines d’années est de savoir si une hausse de la masse monétaire “M” permet d’avoir un effet réel sur
									l’économie (par exemple une hausse de la production réelle) ou bien si cela est simplement créateur d’inflation. Petit
									tour rapide de la pensée classique et de la pensée monétariste.</p>
